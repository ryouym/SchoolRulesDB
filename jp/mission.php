    <p>多くの学校では校則を公開していません。 学校の外からは、生徒たちがどのような指導を受けているのか知ることができません。 まずは学校内を治外法権化や無法地帯化せず、校則を見える化して生徒の権利侵害を見つけられるようにすることが必要です。</p>

