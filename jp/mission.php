    <h1>ブラック校則が生まれた経緯</h1>
    <p>1950年代頃まで校則で学生をことこまかに管理する学校は一部を除いてほとんどありませんでした。</p>
    <p>1960年代末～1970年の学園紛争（学校と学生の闘争）を機に、学校は学生に対して管理を強めていきました。1980年代には中学・高校の校内暴力などの問題が学校内で多発したため、校則を厳しくして校内暴力を力でおさえるような指導が行われるようになりました。生徒の非行を防止する目的で、校則でことこまかに学生を規制するようになりました。</p>

    <h1>ブラック校則はなぜなくならないのか</h1>
    <p>多くの学校は生徒が髪を染めること、 スカートの丈を短くすることは非行の始まりと捉えています。 校則で髪色、スカートの規定することで、非行の芽を摘み、学校の秩序を守ろうとしています。</p>
    <p>学校にとって個別対応ではなく一律禁止、集団管理という対応が最も合理的なため、現代においてもブラック校則は改訂されないまま残っています。</p>

    <h1>ブラック校則は何が問題なのか</h1>
    <p>ブラック校則は生徒の個性をつぶし、想像力を奪います。</p>
